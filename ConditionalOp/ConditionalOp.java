import java.util.*;

public class ConditionalOp {

    public static void main (String args[]) {
        // insert code here...
        char yesNo = 'N';

        switch(yesNo) {
            case 'n':
            case 'N':
                System.out.println("No selected");
                break;
            case 'y':
            case 'Y':
                System.out.println("Yes selected");
                break;
        }
        
                
 
    }
}
